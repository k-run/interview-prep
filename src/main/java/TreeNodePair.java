class TreeNodePair {
    Node root;
    int hd;

    public TreeNodePair(Node root, int hd) {
        this.root = root;
        this.hd = hd;
    }

    public TreeNodePair() {
    }

    public Node getRoot() {
        return root;
    }

    public void setRoot(Node root) {
        this.root = root;
    }

    public int getHd() {
        return hd;
    }

    public void setHd(int hd) {
        this.hd = hd;
    }
}